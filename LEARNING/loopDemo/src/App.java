public class App {
    public static void main(String[] args) throws Exception {

        // ? for döngüsü
        for (int sayma = 0; sayma <= 100; sayma++) {
            System.out.println(sayma);
        }

        // ? while döngüsü

        int has = 8;
        while (has > 2) {
            System.out.println("has 2 den büyük");
            has--;

        }
        System.out.println("has 2 den büyük değil");
        // *do-While döngüsü */
    }
}
